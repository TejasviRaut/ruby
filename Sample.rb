#!/usr/bin/ruby

# class declaration
class Sample

	# similar to the constructor 
	# always called when a new object is created
	def initialize()
		puts "Welcome to ruby"
	end

	# function declaration using def keyword
	def hello
		puts "Hello Ruby!"
	end
end

# creating instance of above class
object = Sample. new

# calling method of above class using object
object.hello


		
